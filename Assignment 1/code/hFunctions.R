dim = 10

#############################################################################################
#
# Function to convert x and y to index
#
#############################################################################################
xy2Index <-function(node)
{
	x <- node[1]
	y <- node[2]
	return( (x-1)*dim + y )
}

#############################################################################################
#
# Function to convert an index to x and y
#
#############################################################################################
index2XY <- function(node)
{
	if( (node%%dim) == 0)
	{
		x <- node/dim
		y <- dim
	}
	else
	{
		x <- (as.integer(node/dim) + 1)
		y <- (node%%dim)
	}
	return( c(x,y) )
}
